/**
 * 商品助手页 — frame-single 卡片放置规则 (359×329px)
 *
 * ── 页面层次关系 ──────────────────────────────────────────────────────────────
 *
 *  页面背景（不在本文件管理范围）：
 *    app-screen_1.png — 固定的商品助手页 App 截图，提供真实页面上下文。
 *    frame-single 卡片叠加在此截图上，x=8, y=296。
 *
 *  frame-single 卡片内部分区（359×329px）：
 *    ┌────────────────────────────────────────┐  y=0
 *    │  HEADER ZONE  (高度 58px，约 18%)       │  ← 渐变色头 + 背景花纹
 *    ├────────────────────────────────────────┤  y=58
 *    │  CONTENT ZONE (高度 271px，约 82%)      │  ← 白色卡片内容区
 *    └────────────────────────────────────────┘  y=329
 *
 * ── HEADER ZONE 设计规则 ──────────────────────────────────────────────────────
 *
 * 头部区域只允许两类元素：
 *
 *   1. 渐变色底（必须）
 *      - 方向：90deg 横向渐变
 *      - 颜色：campaign 主色 → 次色（colors.gradients[gradientIndex]）
 *      - 待补充：与设计师确认具体有哪几套渐变方案（见下方 GRADIENT_VARIANTS）
 *
 *   2. 背景花纹（可选，背景层）
 *      - 定义：装饰性纹样，属于花纹而非前景元素
 *      - 代表素材：梅花枝（plum-blossom）、松树、灯笼（作为纹样小件）
 *      - 位置：紧贴卡片四角，允许略超出卡片边缘
 *      - 规模：scale ≤ 0.30（视觉上退为纹样，不喧宾夺主）
 *      - z-index：低于文字标签，仅作装饰
 *
 * ── 明确不放的元素 ────────────────────────────────────────────────────────────
 *
 *   ✗ 主体素材（hero）：马+灯笼、角色预制件等大型中心元素
 *   ✗ 道具型辅助素材（prop-decor）：红包、话筒等作为"道具"摆放的元素
 *                                   （这些元素可以在大促首页模块使用，
 *                                    但不出现在商品助手卡片头部）
 *
 * ── CONTENT ZONE 说明 ────────────────────────────────────────────────────────
 *
 *   白色卡片区，不在背景生成器管理范围内。
 *   内容由业务数据动态填充，生成器仅提供视觉框架（标题、占位线条）。
 *
 * ── 渐变方案（待与设计师讨论后补全） ─────────────────────────────────────────
 *
 * 商品助手页目前支持通过 content.gradientIndex 切换渐变方案。
 * 具体方案定义在 colors.ts 的 gradients 数组中。
 *
 * 已知方案（待补充实际颜色值）：
 *   gradientIndex = 0: 默认春节红渐变（当前启用）
 *   gradientIndex = 1: （TBD）
 *   gradientIndex = 2: （TBD）
 *
 * ── 花纹素材区间（当前 layout-rules.ts 配置） ────────────────────────────────
 *
 *   frame-single header 可用的花纹元素（在 59×359px 范围内渲染）：
 *   - plum-tl (梅花枝·左):  x=[-4,8]%,  y=[0,10]%,  scale=[0.20,0.28], flipX=true
 *   - plum-tr (梅花枝·右):  x=[76,90]%, y=[0,10]%,  scale=[0.18,0.25]
 *   - envelope-bl (红包·左下): 当前存在，但属于道具类 → 后续评估是否移除
 *   - lantern-br (灯笼·右下):  当前存在，但属于道具类 → 后续评估是否移除
 *
 * Note: envelope 和 lantern 目前在头部底边区域渲染，视觉上类似花纹效果，
 *       但从素材分类上属于"道具"。如后续收到明确的花纹素材（B1/B3 类），
 *       建议替换为专用花纹图案。
 */

